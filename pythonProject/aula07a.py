n1 = int(input("Digite um numero: "))
n2 = int(input("Digite outro numero: "))
s = n1 + n2
sub = n1 - n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
p = n1 ** n2
print("A soma vale {}, a subtração vale {}, a multiplicação vale {} e a divisao vale {}".format(s, sub, m, d))
print("A divisão inteira vale {} e a potencia vale {}".format(di, p))
